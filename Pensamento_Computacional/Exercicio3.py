"""Exercício 3: 

Crie um algoritmo que conte de 1 a 10 e imprima cada número na tela. Use um
loop ENQUANTO."""


#Inicializa o contador em 1:
contador= 1
#Enquanto contador for menor ou igual a 10:
while contador <= 10:
    #Imprime o valor atual do contador:
    print(contador)
    #Incrementa o contador em 1 até cumprir o while:
    contador += 1
